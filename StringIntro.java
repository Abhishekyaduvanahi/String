package stringDSA;

public class StringIntro {

	public static void main(String[] args) {
		String str = "Abhishek Yadav";
		// str.length() --> is used for giving length
		System.out.println(str.length());
		// str.charAt() --> is used to give value at particular points
		System.out.println(str.charAt(4));
		// str.substring() --> is used to provide a substring of a given string
		System.out.println(str.substring(0,str.length()));
		// think about it 
		System.out.println(str.substring(6));

	}

}
